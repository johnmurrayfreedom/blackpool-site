// Function to get all article categories
export function getAllCategories() {
  const categories = allNewsArticles.map(article => article.category);
  return Array.from(new Set(categories));
}

// Function to get all article tags
export function getAllTags() {
  const tags = allNewsArticles.flatMap(article => article.tags);
  return Array.from(new Set(tags));
}

// Function to get articles by category
export function getArticlesByCategory(category: string) {
  return allNewsArticles.filter(article => article.category === category);
}

// Function to get articles by tag
export function getArticlesByTag(tag: string) {
  return allNewsArticles.filter(article => article.tags.includes(tag));
}

// Import topical articles
import { topicalArticles } from './topicalArticles';

// Combine news and topical articles for search
const allArticles = [...allNewsArticles, ...topicalArticles];

// Function to search articles
export function searchArticles(query: string) {
  const lowerCaseQuery = query.toLowerCase();
  return allArticles.filter(article => 
    article.title.toLowerCase().includes(lowerCaseQuery) || 
    article.excerpt.toLowerCase().includes(lowerCaseQuery) || 
    article.content.some(section => 
      section.type === 'paragraph' && 
      section.content && 
      section.content.toLowerCase().includes(lowerCaseQuery)
    )
  );
}